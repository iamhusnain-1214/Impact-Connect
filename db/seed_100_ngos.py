"""
Seeds the database with 100+ NGOs for realistic directory testing.

IMPORTANT - be upfront about what this actually contains:
  - 15 REAL, verifiable Pakistani NGOs (imported from seed_real_ngos.py),
    marked verified=True. These are genuine organizations.
  - ~90 SYNTHETIC sample entries, generated by combining real Pakistani
    cities with real cause categories (e.g. "Multan Water Trust"). These
    are NOT real organizations - they exist purely to give the directory
    enough volume to test search, filtering, and pagination realistically.
    They are marked verified=False so nothing implies they're legitimate,
    audited NGOs.

Run: python -m db.seed_100_ngos
"""

import random
from db.connection import get_connection
from db.seed_real_ngos import REAL_NGOS, PLACEHOLDER_NAMES

random.seed(42)  # reproducible output every run

