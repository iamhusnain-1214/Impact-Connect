def generate_synthetic_ngos(count):
    """Generates `count` unique synthetic NGOs from city x category x suffix combinations."""
    causes = list(CAUSE_TEMPLATES.keys())
    combos = []
    for city, province in CITIES:
        for cause in causes:
            for suffix in ORG_SUFFIXES:
                combos.append((city, province, cause, suffix))
    random.shuffle(combos)

    ngos = []
    seen_names = set()
    for city, province, cause, suffix in combos:
        name = f"{city} {cause} {suffix}"
        if name in seen_names:
            continue
        seen_names.add(name)

        desc_template, mission_template = CAUSE_TEMPLATES[cause]
        ngos.append({
            "name": name,
            "description": desc_template.format(city=city),
            "mission": mission_template.format(city=city),
            "city": city,
            "province": province,
            "website": "",
            "categories": [cause],
        })
        if len(ngos) >= count:
            break
    return ngos


def seed():
    conn = get_connection()
    cursor = conn.cursor(dictionary=True)

    try:
        for name in PLACEHOLDER_NAMES:
            cursor.execute("DELETE FROM ngos WHERE name = %s", (name,))
        conn.commit()

        cursor.execute("SELECT category_id, name FROM categories")
        cat_lookup = {row["name"]: row["category_id"] for row in cursor.fetchall()}

        def insert_ngo(ngo, verified):
            cursor.execute("SELECT ngo_id FROM ngos WHERE name = %s", (ngo["name"],))
            if cursor.fetchone():
                return False
            cursor.execute(
                """INSERT INTO ngos (name, description, mission, city, province, contact, website, verified)
                   VALUES (%s, %s, %s, %s, %s, %s, %s, %s)""",
                (ngo["name"], ngo["description"], ngo["mission"], ngo["city"], ngo["province"],
                 "See website for contact details", ngo.get("website", ""), verified)
            )
            ngo_id = cursor.lastrowid
            for cat_name in ngo["categories"]:
                cat_id = cat_lookup.get(cat_name)
                if cat_id:
                    cursor.execute(
                        "INSERT INTO ngo_categories (ngo_id, category_id) VALUES (%s, %s)",
                        (ngo_id, cat_id)
                    )
            return True

        real_count = 0
        for ngo in REAL_NGOS:
            if insert_ngo(ngo, verified=True):
                real_count += 1

        synthetic = generate_synthetic_ngos(count=95)
        synthetic_count = 0
        for ngo in synthetic:
            if insert_ngo(ngo, verified=False):
                synthetic_count += 1

        conn.commit()
        total = real_count + synthetic_count
        print(f"Inserted {real_count} real, verified NGOs.")
        print(f"Inserted {synthetic_count} synthetic sample NGOs (unverified, for demo volume).")
        print(f"Total NGOs in directory: {total}")

    except Exception:
        conn.rollback()
        raise
    finally:
        cursor.close()
        conn.close()


if __name__ == "__main__":
    seed()
